x = float(input("Введите x: "))  # Считываем значение x 
epsilon = float(input("Введите ε (точность): "))  # Считываем точность 

sum_value = 0.0  # Инициализация суммы ряда 
term = 1.0  # Инициализация первого члена ряда 
k = 0  # Инициализация номера члена ряда 
factorial = 1  # Инициализация факториала (1! = 1)

print(f"{'Шаг':<5} {'Текущий член':<20} {'Сумма':<20}")  # Заголовок для вывода

while abs(term) >= epsilon:
    if k > 0:
        factorial *= k  # Вычисляем (k + 1)! через предыдущий факториал
        term *= -((x / 2)**2) / (k**2)  # Оптимизация: вычисляем текущий член через предыдущий
    else:
        term = 1.0  # Первый член ряда

    sum_value += term  # Добавляем текущий член к общей сумме
    print(f"{k:<5} {term:<20.10f} {sum_value:<20.10f}")  # Вывод текущего шага
    
    k += 1  # Переходим к следующему члену ряда

# Выводим результат
print(f"\nПриближенное значение суммы ряда: {sum_value:.6f}")
print(f"Количество шагов: {k}")