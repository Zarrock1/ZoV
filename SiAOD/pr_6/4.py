def build_matrix(arr):
    n = len(arr)  # Определяем длину массива
    matrix = []  # Инициализируем пустой список для хранения матрицы
    for i in range(n):  # Проходим по каждому индексу от 0 до n-1
        row = arr[i:] + arr[:i]  # Формируем строку матрицы с циклическим сдвигом элементов
        matrix.append(row)  # Добавляем сформированную строку в матрицу
    return matrix  # Возвращаем полученную матрицу

# Ввод массива с клавиатуры
A = list(map(int, input("Введите элементы массива через пробел: ").split()))  # Считываем элементы массива, преобразуем их в список целых чисел

# Построение матрицы
matrix = build_matrix(A)  # Вызываем функцию для построения матрицы

# Выводим результат
for row in matrix:  # Проходим по каждой строке матрицы
    print(row)  # Выводим строку
