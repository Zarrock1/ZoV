# Ввод степеней полиномов
degree1 = int(input("Введите степень первого полинома: "))  # Запрашиваем у пользователя степень первого полинома
degree2 = int(input("Введите степень второго полинома: "))  # Запрашиваем у пользователя степень второго полинома

# Ввод коэффициентов первого полинома
print("Введите коэффициенты первого полинома от старшей степени к нулевой:")
coeffs1 = []  # Пустой список для хранения коэффициентов первого полинома
for i in range(degree1, -1, -1):  # Проходим по степеням полинома от старшей (degree1) к младшей (0)
    coeff = float(input(f"Коэффициент при x^{i}: "))  # Запрашиваем у пользователя коэффициент при текущей степени x^i
    coeffs1.append(coeff)  # Добавляем введённый коэффициент в список coeffs1

# Ввод коэффициентов второго полинома
print("Введите коэффициенты второго полинома от старшей степени к нулевой:")
coeffs2 = []  # Пустой список для хранения коэффициентов второго полинома
for i in range(degree2, -1, -1):  # Проходим по степеням полинома от старшей (degree2) к младшей (0)
    coeff = float(input(f"Коэффициент при x^{i}: "))  # Запрашиваем у пользователя коэффициент при текущей степени x^i
    coeffs2.append(coeff)  # Добавляем введённый коэффициент в список coeffs2

# Выравнивание длин массивов коэффициентов
max_degree = max(degree1, degree2)  # Определяем максимальную степень среди двух полиномов
# Дополняем списки коэффициентов нулями, чтобы длины списков были равны максимальной степени + 1
coeffs1_extended = [0]*(max_degree - degree1) + coeffs1  # Добавляем нули в начале списка coeffs1, если степень меньше
coeffs2_extended = [0]*(max_degree - degree2) + coeffs2  # Добавляем нули в начале списка coeffs2, если степень меньше

# Сложение коэффициентов
result_coeffs = []  # Пустой список для хранения коэффициентов результирующего полинома
for c1, c2 in zip(coeffs1_extended, coeffs2_extended):  # Проходим по парам коэффициентов из обоих полиномов
    result_coeffs.append(c1 + c2)  # Складываем коэффициенты для одинаковых степеней и добавляем в result_coeffs

# Вывод результата
print("Коэффициенты результирующего полинома от старшей степени к нулевой:")
for i, coeff in enumerate(result_coeffs):  # Проходим по коэффициентам результирующего полинома
    print(f"Коэффициент при x^{max_degree - i}: {coeff}")  # Выводим каждый коэффициент вместе с его степенью
