def calculate_sum(n):
    S = 0  # Инициализируем переменную S для хранения суммы
    for i in range(1, n + 1):  # Цикл от 1 до n включительно
        term = (1 if i % 2 != 0 else -1) / (i * (i + 1))  # Вычисляем текущий член ряда с учетом знака
        S += term  # Добавляем текущий член к общей сумме S
    return S  # Возвращаем итоговую сумму S

# Ввод n
n = int(input("Введите натуральное число n: "))  # Считываем натуральное число n от пользователя

# Вычисление суммы
result = calculate_sum(n)  # Вызываем функцию calculate_sum для вычисления суммы ряда

# Вывод результата
print(f"Сумма ряда: {result}")  # Выводим результат на экран
