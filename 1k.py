x = float(input("Введите значение x: "))  # Ввод значения x с клавиатуры
epsilon = float(input("Введите значение точности epsilon: "))  # Ввод точности epsilon с клавиатуры

k = 1  # Индекс для ряда, начиная с 1
total_sum = 0  # Итоговая сумма ряда

while True:
    term = x**(2 * k) / 3**k  # Вычисляем текущий член ряда
    if abs(term) <= epsilon:  # Проверяем, если член меньше или равен точности
        break  # Прерываем цикл
    total_sum += term  # Добавляем текущий член к сумме
    k += 1  # Увеличиваем индекс

print(f"Сумма ряда при x = {x}, epsilon = {epsilon}: {total_sum}")  # Вывод результата
